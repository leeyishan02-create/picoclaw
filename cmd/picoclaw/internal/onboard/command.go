// =============================================================================
// onboard 包 - 入门引导模块
// =============================================================================
// 这个包负责PicoClaw的首次使用引导功能。
// 首次运行PicoClaw时，需要运行此命令来初始化配置和工作区。
// =============================================================================

package onboard

// =============================================================================
// 导入 (Imports)
// =============================================================================
import (
	"embed" // Go 1.16+ 的嵌入文件功能

	"github.com/spf13/cobra" // CLI命令库
)

// =============================================================================
// 嵌入文件 (Embedded Files)
// =============================================================================
// 使用Go的embed功能将workspace目录嵌入到二进制文件中
// 这样用户在首次运行onboard时，工作区文件会被自动创建
//

var embeddedFiles embed.FS

// =============================================================================
// NewOnboardCommand - 创建入门引导命令
// =============================================================================
// 这是onboard模块的入口函数，创建入门引导命令。
// 当用户运行"picoclaw onboard"时会调用此函数。
//
// 功能说明：
// - 创建配置文件目录 ~/.picoclaw/
// - 创建工作区目录
// - 复制示例工作区文件
// - 设置默认配置
//
// 使用示例：
//
//	picoclaw onboard
//	picoclaw o
func NewOnboardCommand() *cobra.Command {
	cmd := &cobra.Command{
		Use:     "onboard",
		Aliases: []string{"o"}, // 命令别名
		Short:   "Initialize picoclaw configuration and workspace",
		Run: func(cmd *cobra.Command, args []string) {
			onboard()
		},
	}

	return cmd
}
