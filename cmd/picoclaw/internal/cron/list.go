package cron

// =============================================================================
// 导入 (Imports)
// =============================================================================
import "github.com/spf13/cobra"

// =============================================================================
// newListCommand - 创建列出命令
// =============================================================================
// 创建"picoclaw cron list"子命令（别名"l"）。
// 显示所有已创建的定时任务。
//
// 参数：
//   - storePath: 返回定时任务存储文件路径的函数
//
// 使用示例：
//
//	picoclaw cron list
//	picoclaw cron l
func newListCommand(storePath func() string) *cobra.Command {
	cmd := &cobra.Command{
		Use:   "list",
		Short: "List all scheduled jobs",
		Args:  cobra.NoArgs,
		RunE: func(_ *cobra.Command, _ []string) error {
			cronListCmd(storePath())
			return nil
		},
	}

	return cmd
}
