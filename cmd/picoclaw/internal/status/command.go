// =============================================================================
// status 包 - 状态查看模块
// =============================================================================
// 这个包负责PicoClaw的状态查看功能。
// 用户可以查看PicoClaw的当前运行状态。
// =============================================================================

package status

// =============================================================================
// 导入 (Imports)
// =============================================================================
import (
	"github.com/spf13/cobra" // CLI命令库
)

// =============================================================================
// NewStatusCommand - 创建状态命令
// =============================================================================
// 这是status模块的入口函数，创建状态查看命令。
// 当用户运行"picoclaw status"时会调用此函数。
//
// 功能说明：
// - 显示PicoClaw版本信息
// - 显示配置状态
// - 显示已配置的通道
// - 显示认证状态
//
// 使用示例：
//
//	picoclaw status
//	picoclaw s
func NewStatusCommand() *cobra.Command {
	cmd := &cobra.Command{
		Use:     "status",
		Aliases: []string{"s"}, // 命令别名
		Short:   "Show picoclaw status",
		Run: func(cmd *cobra.Command, args []string) {
			statusCmd()
		},
	}

	return cmd
}
