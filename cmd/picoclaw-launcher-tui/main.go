// =============================================================================
// PicoClaw Launcher TUI - 终端用户界面版本
// =============================================================================
// 这是一个基于终端的用户界面（TUI）版本的PicoClaw启动器。
// 使用现代终端UI库提供交互式配置体验。
//
// 主要特点：
// - 终端界面，无需浏览器
// - 键盘导航支持
// - 实时配置预览
// - 跨平台支持（Windows、macOS、Linux）
//
// 构建和使用方法：
//   go build -o picoclaw-launcher-tui ./cmd/picoclaw-launcher-tui/
//   ./picoclaw-launcher-tui
// =============================================================================

package main

// =============================================================================
// 导入 (Imports)
// =============================================================================
import (
	"fmt" // 格式化输出
	"os"  // 操作系统功能

	"github.com/sipeed/picoclaw/cmd/picoclaw-launcher-tui/internal/ui" // TUI界面
)

// =============================================================================
// main 函数 - 程序入口点
// =============================================================================
// 启动TUI界面并运行应用程序。
// 如果发生错误，打印错误信息并以退出码1退出。
func main() {
	if err := ui.Run(); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}
